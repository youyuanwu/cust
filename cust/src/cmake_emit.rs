/// Whole-workspace view the emitter consumes. Owned (not borrowed
/// against the live `Workspace`) so unit tests can fabricate
/// inputs without standing up a manifest tree.
#[derive(Debug, Clone)]
pub struct WorkspaceView {
    /// cust version string baked into the `@generated by` header
    /// comment. V42D-4: version is part of the byte format and
    /// therefore part of the configure-skip stamp; a cust upgrade
    /// that changes this string churns the stamp on purpose.
    pub cust_version: String,
    /// `CMAKE_C_STANDARD` value, e.g. `"23"`. v0.4.2 hard-codes
    /// to 23 in practice (matches clang ‚â• 19 default for cwork);
    /// surfaced as a field so slice B can plumb `[clang] std` /
    /// per-member overrides through later.
    pub c_standard: String,
    /// Absolute path to the cust clang plugin .so, resolved by
    /// `plugin::Plugin::discover()`. `None` is permitted so the
    /// emitter stays usable in test environments without a real
    /// plugin; slice B's `emit_and_drive_cmake` rejects it before
    /// invoking `cmake`. The path itself is no longer emitted by
    /// `generate` directly (each member bakes the plugin flag into
    /// its own `compile_options`); the field stays on
    /// `WorkspaceView` because it still feeds the configure-skip
    /// stamp (V42D-8 + RQ-V42-1: plugin bytes hash into the stamp).
    pub plugin_path: Option<PathBuf>,
    /// Members in topological (build) order: dependencies before
    /// consumers. V42D-4 + same rule the existing
    /// `Workspace::build_order` applies.
    pub members: Vec<MemberView>,
}

/// One workspace member. Slice A emits at most two `CMake` targets
/// per member: one library + one executable. Multi-bin (v0.4.4)
/// will extend `bin_sources` from `Vec<SourceFile>` to a list of
/// `(target_name, sources)` tuples; not in slice A scope.
#[derive(Debug, Clone)]
pub struct MemberView {
    /// `[package].name`. Used verbatim as the `CMake` target name.
    pub name: String,
    /// What the member produces. `LibOnly` / `BinOnly` /
    /// `LibAndBin`. Mirrors `manifest::CrateKind` but stays
    /// `CrateKind`-free at the emitter boundary so tests don't
    /// have to construct a full `Manifest`.
    pub kind: MemberKind,
    /// Lib-half sources in topological order over `#cust use
    /// crate::<mod>` edges (= the `<crate>.h` concatenation
    /// order; V42D-4). Empty when `kind` is `BinOnly`.
    pub lib_sources: Vec<SourceFile>,
    /// Bin-half sources in the same topological order. Empty
    /// when `kind` is `LibOnly`.
    pub bin_sources: Vec<SourceFile>,
    /// Absolute path the lib archive is placed in via
    /// `ARCHIVE_OUTPUT_DIRECTORY`. Preserves the
    /// `target/<profile>/build/<crate>/` layout from v0.3+
    /// (V42D-13 keeps per-crate driver outputs at their old
    /// paths even though the `CMake` tree is workspace-level).
    pub archive_output_dir: PathBuf,
    /// Include dirs the bin half needs to reach. For a
    /// lib+bin member this is the crate's own lib include dir
    /// (so `main.c` can `#include "<crate>.h"`). For pure-bin
    /// members it's typically the per-crate `build/<crate>/include/`
    /// of each workspace dep (publish-via-symlink dep view from
    /// v0.3 still works as include resolution).
    ///
    /// Order is preserved verbatim; the caller is responsible
    /// for determinism (V42D-4: callers pass topo-ordered lists).
    pub bin_include_dirs: Vec<PathBuf>,
    /// Names of other workspace members this member's bin half
    /// links against, in topological order. Lowered to
    /// `target_link_libraries(<bin> PRIVATE <dep>)` per V42D-13.
    /// Empty for lib-only members.
    pub workspace_link_deps: Vec<String>,
    /// Names of other workspace members this member's *library*
    /// half depends on, in topological order. Lowered to
    /// `target_link_libraries(<lib> PUBLIC <dep>)` so `CMake`'s
    /// `--target <lib>` walker pulls each dep into the build
    /// (`cust build -p <lib-member>` parity with Cargo). PUBLIC
    /// also propagates the link to downstream consumers, so an
    /// exe linking against `<lib>` transparently picks up the
    /// dep chain. Empty for bin-only members.
    pub lib_workspace_deps: Vec<String>,
    /// Per-target compile options applied to every TU of the
    /// member's library and executable targets via
    /// `target_compile_options(<t> PRIVATE ‚Ä¶)`. Built by the
    /// caller from: profile cflags (opt-level, debug info,
    /// sanitisers), `[clang] extra-cflags` from the manifest,
    /// `-fvisibility=hidden`, `-include <prelude>`, and (when a
    /// plugin is present) `SHELL:-fplugin=<abs>` +
    /// `-Wno-unknown-attributes`. The emitter passes them
    /// through verbatim ‚Äî each entry becomes one literal `CMake`
    /// list element. Wrap shell-significant entries (e.g. the
    /// plugin `-fplugin=‚Ä¶` arg) in a `SHELL:` prefix at the
    /// caller to bypass `CMake`'s de-dup / re-order pass.
    pub compile_options: Vec<String>,
    /// Absolute path the bin executable is placed in via
    /// `RUNTIME_OUTPUT_DIRECTORY`. Cargo-parity layout: bins
    /// land at `target/<profile>/<crate>/` so `cust run` finds
    /// them at the v0.3+ location. Ignored for lib-only members.
    pub runtime_output_dir: PathBuf,
    /// `CMake` target name for the bin half. For pure-bin members
    /// this equals `name`; for lib+bin it's suffixed `-bin` to
    /// avoid the `add_executable cannot create target "<name>"
    /// because another target with the same name already exists`
    /// error (`CMake` target names are workspace-unique). The bin's
    /// `OUTPUT_NAME` still emits the unsuffixed `name` so the on-
    /// disk file is `<name>` either way ‚Äî only the in-`CMake`
    /// target identifier differs.
    pub bin_target_name: String,
    /// V42D-14 test executable. `Some` whenever the member has
    /// a lib half (V32D-11 ‚Äî `cust test` only tests library
    /// crates; lib+bin members test their library half only).
    /// The emitter marks each test target `EXCLUDE_FROM_ALL`
    /// so `cust build` skips it; `cust test` reaches it via
    /// `cmake --build --target <crate>__test`. Same `CMakeLists`
    /// bytes either way ‚Äî the V42D-8 stamp doesn't churn when
    /// flipping modes, so neither does the configure step.
    pub test_target: Option<TestTargetView>,
}

/// V42D-14 per-member test executable description. Lowered to a
/// `add_executable(<crate>__test EXCLUDE_FROM_ALL ...)` target.
/// `EXCLUDE_FROM_ALL` keeps it out of the default `cmake --build`
/// graph; `cust test` reaches it explicitly via `--target`.
#[derive(Debug, Clone)]
pub struct TestTargetView {
    /// `CMake` target name. Always `<member>__test` (V42D-14).
    pub target_name: String,
    /// Test-build sources: same lib-half `.rewrite/<crate>/src/*.c`
    /// files as the lib target, plus the driver-generated
    /// `cust_test_main_<crate>.c` runner TU.
    pub sources: Vec<SourceFile>,
    /// Include dirs the test TUs need to reach. Slice C: the
    /// member's own `build/<crate>/include/` so the runner can
    /// `#include "<crate>.h"` if needed.
    pub include_dirs: Vec<PathBuf>,
    /// Workspace deps to link against (transitive lib chain) ‚Äî
    /// same shape as `MemberView::workspace_link_deps`.
    pub link_deps: Vec<String>,
    /// Per-target compile options. Same shape as
    /// `MemberView::compile_options` plus the
    /// `-DCUST_TEST_BUILD=1` define and the `cust test` plugin
    /// invocation (no different from the build-mode plugin
    /// argv except that plugin-side branches keyed on
    /// `CUST_TEST_BUILD` activate).
    pub compile_options: Vec<String>,
    /// Absolute path the test exe lands in via
    /// `RUNTIME_OUTPUT_DIRECTORY`. Slice C pins this at
    /// `target/<profile>/test/<crate>/` so the existing test
    /// runner code (and integration test fixtures) find the
    /// binary at the v0.3.2 location.
    pub runtime_output_dir: PathBuf,
}

/// Mirror of `manifest::CrateKind`, exposed at the emitter
/// boundary so unit tests can construct inputs without dragging
/// the whole manifest module in.
#[derive(Debug, Clone, Copy, PartialEq, Eq)]
pub enum MemberKind {
    LibOnly,
    BinOnly,
    LibAndBin,
}

impl MemberKind {
    const fn has_lib(self) -> bool {
        matches!(self, Self::LibOnly | Self::LibAndBin)
    }
    const fn has_bin(self) -> bool {
        matches!(self, Self::BinOnly | Self::LibAndBin)
    }
}

/// One source file passed to `CMake`. Path is absolute; the emitter
/// quotes it directly (no `${CMAKE_CURRENT_LIST_DIR}` rewriting ‚Äî
/// the caller has already resolved everything).
#[derive(Debug, Clone)]
pub struct SourceFile {
    /// Absolute path to the post-`#cust use`-rewrite source file
    /// (`target/<profile>/.rewrite/<crate>/<‚Ä¶>.c` once slice B
    /// moves the rewrite output there; today's preprocessed
    /// path works equally well at slice A since the emitter
    /// isn't load-bearing).
    pub path: PathBuf,
    /// Absolute paths to fragment headers this TU imports via
    /// `#cust use crate::<importee>`, in topological order over
    /// the dep edges (V42D-4 + V42D-6). Empty for TUs with no
    /// cross-module imports. Lowered to a `CMake`
    /// `set_source_files_properties(... OBJECT_DEPENDS ...)`
    /// block per V42D-6.
    pub object_depends: Vec<PathBuf>,
}

