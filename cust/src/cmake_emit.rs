/// Render the workspace `CMakeLists` for `view`. Pure function;
/// determinism is the caller's responsibility (the emitter
/// preserves the orders it's handed).
///
/// Output shape pinned by the golden file at
/// `src/cmake_emit/testdata/cwork.cmake` (V42D-4).
#[must_use]
pub fn generate(view: &WorkspaceView) -> String {
    let mut out = String::new();

    // Header (V42D-4: cust version in `@generated`, no timestamp).
    let _ = writeln!(
        out,
        "# @generated by cust v{} — DO NOT EDIT",
        view.cust_version,
    );
    out.push_str("# Regenerated every `cust build`. Edits are lost.\n");
    out.push_str("cmake_minimum_required(VERSION 3.21)\n");
    out.push_str("project(cust_workspace LANGUAGES C)\n");
    out.push('\n');
    let _ = writeln!(out, "set(CMAKE_C_STANDARD {})", view.c_standard);
    out.push_str("set(CMAKE_C_STANDARD_REQUIRED ON)\n");
    out.push_str("set(CMAKE_EXPORT_COMPILE_COMMANDS ON)\n");

    // Per-member blocks.
    for m in &view.members {
        emit_rewrite_commands(&mut out, &view.cust_exe, m);
        emit_surface_commands(&mut out, &view.cust_exe, m);
        emit_surface_cycle_commands(&mut out, &view.cust_exe, m);
        if m.kind.has_lib() {
            emit_library(&mut out, m);
        }
        emit_crate_header_command(&mut out, &view.cust_exe, m);
        if m.kind.has_bin() {
            emit_executable(&mut out, m);
        }
        if let Some(t) = &m.test_target {
            emit_test_sidecar_commands(&mut out, &view.cust_exe, m);
            emit_test_runner_command(&mut out, &view.cust_exe, m);
            emit_test_executable(&mut out, m, t);
        }
        for it in &m.integration_tests {
            emit_integration_test_executable(&mut out, m, it);
        }
    }

    out
}

/// v0.4.5 V45D-3: emit one `add_custom_command` per lib + bin
/// source so Ninja produces the `.rewrite/<crate>/<rel>.c` files
/// lazily (only when their `src/<…>.c` input changed) instead of
/// the driver rewriting every source eagerly on every build. The
/// outputs are SOURCEs of the consuming lib/exe targets, so they
/// anchor themselves into the graph (V45D-14). One `internal
/// rewrite-file` callback per command.
fn emit_rewrite_commands(out: &mut String, cust_exe: &Path, m: &MemberView) {
    if m.rewrites.is_empty() {
        return;
    }
    out.push('\n');
    let _ = writeln!(out, "# ---- crate: {} (generated rewrites) ----", m.name);
    for rw in &m.rewrites {
        out.push_str("add_custom_command(\n");
        let _ = writeln!(out, "    OUTPUT \"{}\"", rw.out.display());
        let _ = writeln!(
            out,
            "    COMMAND \"{}\" internal rewrite-file",
            cust_exe.display()
        );
        let _ = writeln!(out, "            --crate-name {}", rw.crate_name);
        let _ = writeln!(out, "            --in \"{}\"", rw.origin.display());
        let _ = writeln!(out, "            --out \"{}\"", rw.out.display());
        let _ = writeln!(
            out,
            "            --frags-dir \"{}\"",
            rw.frags_dir.display()
        );
        let _ = writeln!(
            out,
            "            --deps-root \"{}\"",
            rw.deps_root.display()
        );
        let _ = writeln!(
            out,
            "            --own-lib-header \"{}\"",
            rw.own_lib_header.display()
        );
        for dep in &rw.deps {
            let _ = writeln!(out, "            --dep {dep}");
        }
        if rw.is_bin_half {
            out.push_str("            --bin-half\n");
        }
        if rw.has_lib {
            out.push_str("            --has-lib\n");
        }
        let _ = writeln!(out, "    DEPENDS \"{}\"", rw.origin.display());
        out.push_str("    VERBATIM)\n");
    }
}

