/// Walk `ws` and produce a `WorkspaceView` describing every
/// member's lib/bin shape, sources (post-`#cust use` rewrite
/// paths), `OBJECT_DEPENDS` fragment edges, per-target compile
/// options, and output directories.
///
/// Slice B (V42D-16) is the first real consumer: the emitter
/// drives a `cmake -G Ninja` + `cmake --build` invocation
/// against the result. Walk is deterministic — same inputs in,
/// same `WorkspaceView` out, byte-stable through `generate`.
///
/// The view always contains every workspace member, regardless of
/// `cust build -p <name>` scoping (V42D-13: one `CMakeLists` for
/// the whole workspace; `-p` lowers at `cmake --build --target`
/// time, not at emit time). This keeps the stamp stable across
/// `-p` variations.
///
/// When the member has a lib half, the view always also carries
/// a populated `test_target` (V42D-14). The emitter marks the
/// test target `EXCLUDE_FROM_ALL` so the same `CMakeLists`
/// serves both `cust build` and `cust test` — only the
/// `cmake --build --target` selection on the build step
/// differs. The V42D-8 stamp is therefore stable across mode
/// flips, and `CMake` doesn't reconfigure when toggling between
/// build and test in the same workspace.
///
/// v0.4.5 V45D-3/V45D-4/V45D-5: the lib half's contribution to a
/// member's `MemberView`, returned by [`collect_lib_artifacts`].
struct LibArtifacts {
    sources: Vec<SourceFile>,
    rewrites: Vec<RewriteCommand>,
    surface_commands: Vec<SurfaceCommand>,
    surface_cycles: Vec<SurfaceCycleCommand>,
    crate_header: Option<CrateHeaderCommand>,
    test_sidecars: Vec<TestSidecarCommand>,
    test_runner: Option<TestRunnerCommand>,
}

/// v0.4.5 V45D-3/V45D-4/V45D-5: collect the lib half's sources,
/// rewrite commands, surface commands, and crate-header command
/// for one member. Returns empty vecs + `None` for a bin-only
/// member (no lib source). Extracted from `collect_view` to keep
/// it under the line cap.
fn collect_lib_artifacts(
    m: &crate::workspace::Member,
    rewrite_root: &Path,
    layout: &TargetLayout,
    gen_ctx: &MemberGenCtx,
) -> Result<LibArtifacts> {
    let Some(src) = m.kind.lib_source() else {
        return Ok(LibArtifacts {
            sources: Vec::new(),
            rewrites: Vec::new(),
            surface_commands: Vec::new(),
            surface_cycles: Vec::new(),
            crate_header: None,
            test_sidecars: Vec::new(),
            test_runner: None,
        });
    };
    let c = collect_sources(
        &m.root,
        src,
        &m.name,
        rewrite_root,
        layout,
        gen_ctx,
        false,
        true,
    )?;
    // V45D-5: every lib-bearing member publishes a crate header
    // concatenating its fragments (in topological order).
    let header = CrateHeaderCommand {
        crate_name: m.name.clone(),
        out: layout.crate_header_path(&m.name),
        frags: c.header_frags,
    };
    Ok(LibArtifacts {
        sources: c.sources,
        rewrites: c.rewrites,
        surface_commands: c.surface_commands,
        surface_cycles: c.surface_cycles,
        crate_header: Some(header),
        test_sidecars: c.test_sidecars,
        test_runner: c.test_runner,
    })
}

/// v0.4.4 V44D-5/V44D-8: build one `BinView` per `BinTarget` of a
/// member. The package bin (name == member name) keeps the v0.3.1
/// target name (`<crate>` pure-bin / `<crate>-bin` lib+bin); extra
/// bins get `<crate>__bin__<name>`. The on-disk `OUTPUT_NAME` is
/// always the bin name. Each bin's sources are its own rewritten
/// module graph.
fn collect_bin_views(
    m: &crate::workspace::Member,
    kind: MemberKind,
    rewrite_root: &Path,
    layout: &TargetLayout,
    rw_ctx: &MemberGenCtx,
) -> Result<(Vec<BinView>, Vec<RewriteCommand>)> {
    let mut bins: Vec<BinView> = Vec::with_capacity(m.kind.bins().len());
    let mut rewrites: Vec<RewriteCommand> = Vec::new();
    // V45D-3: a bin source of a lib+bin crate is the "bin half"
    // (enables the `#cust use <crate>;` own-lib carve-out).
    let is_bin_half = kind == MemberKind::LibAndBin;
    for bt in m.kind.bins() {
        let collected = collect_sources(
            &m.root,
            &bt.source,
            &m.name,
            rewrite_root,
            layout,
            rw_ctx,
            is_bin_half,
            false,
        )?;
        rewrites.extend(collected.rewrites);
        let target_name = if bt.name == m.name {
            if kind == MemberKind::LibAndBin {
                format!("{}-bin", m.name)
            } else {
                m.name.clone()
            }
        } else {
            format!("{}__bin__{}", m.name, bt.name)
        };
        bins.push(BinView {
            target_name,
            output_name: bt.name.clone(),
            sources: collected.sources,
        });
    }
    Ok((bins, rewrites))
}

pub fn collect_view(
    ws: &Workspace,
    profile_kind: ProfileKind,
    plugin: Option<&Plugin>,
    default_std: &str,
) -> Result<WorkspaceView> {
    let layout = TargetLayout::for_workspace(&ws.root, profile_kind);
    let rewrite_root = layout.profile_root.join(".rewrite");
    let prelude_path = layout.prelude_path();
    // V42D-14: cust_test_main_<crate>.c files live under the
    // workspace CMake dir so they're alongside the CMakeLists
    // that references them.
    let cmake_dir = layout.profile_root.join("cmake");

    let mut members: Vec<MemberView> = Vec::with_capacity(ws.members.len());
    for m in &ws.members {
        let kind = map_kind(&m.kind);
        // Per-target compile options + the surface command's
        // cflags both derive from the resolved profile, so resolve
        // it once up front (V45D-15).
        let profile_override = match profile_kind {
            ProfileKind::Dev => m.manifest.profile.dev.as_ref(),
            ProfileKind::Release => m.manifest.profile.release.as_ref(),
        };
        let profile = ResolvedProfile::resolve(profile_kind, profile_override)?;
        // V45D-3/V45D-4: per-member generation context (shared by
        // the lib + every bin source, and the surface commands).
        let gen_ctx = member_gen_ctx(
            m,
            kind,
            &layout,
            &profile,
            &prelude_path,
            plugin,
            default_std,
        );
        let lib = collect_lib_artifacts(m, &rewrite_root, &layout, &gen_ctx)?;
        let lib_sources = lib.sources;
        let mut rewrites = lib.rewrites;
        let surface_commands = lib.surface_commands;
        let surface_cycles = lib.surface_cycles;
        let crate_header = lib.crate_header;
        let test_sidecars = lib.test_sidecars;
        let test_runner = lib.test_runner;
        // v0.4.4 V44D-5/V44D-8: one `BinView` per `BinTarget`.
        let (bins, bin_rewrites) = collect_bin_views(m, kind, &rewrite_root, &layout, &gen_ctx)?;
        rewrites.extend(bin_rewrites);

        // Bin half include dirs: for a lib+bin member, the bin
        // needs the lib's own include dir for `#include "<crate>.h"`
        // (matches the existing `compile_one_module` extra_includes
        // shape). Cross-crate dep headers are reached via the
        // absolute paths baked by `#cust use <dep>` lowering, not
        // via `target_include_directories`.
        let bin_include_dirs = if kind == MemberKind::LibAndBin {
            vec![layout
                .crate_header_path(&m.name)
                .parent()
                .unwrap()
                .to_path_buf()]
        } else {
            Vec::new()
        };

        // Intra-workspace link deps for the bin half (V42D-13:
        // lower to `target_link_libraries`). Workspace deps come
        // from `Member::deps`; for lib+bin we additionally link
        // against the member's own lib half (which still has the
        // unsuffixed member name as its CMake target).
        let mut workspace_link_deps = m.deps.clone();
        if kind == MemberKind::LibAndBin {
            workspace_link_deps.insert(0, m.name.clone());
        }

        // CMake target names for the bins are computed above in the
        // `BinView` loop (V44D-5).

        // Per-target compile options. Mirror what `build::build_cflags`
        // would produce for a non-test, non-syntax-only TU minus the
        // trailing `-c -o <obj> <src>` (CMake supplies those).
        let compile_options =
            build_member_compile_options(&profile, &m.manifest, &prelude_path, plugin);

        // V42D-14 test target (whenever the member has a lib
        // half). Always populated so the emitted CMakeLists is
        // identical between `cust build` and `cust test`;
        // `EXCLUDE_FROM_ALL` on the target keeps it out of the
        // default build graph.
        let test_target =
            collect_test_target(m, kind, &lib_sources, &compile_options, &cmake_dir, &layout);

        // v0.4.3 V43D-5: one integration-test target per
        // `tests/<stem>.c`. Only members with a lib half get
        // them (V43D-3: integration tests link `lib<crate>.a`);
        // bin-only members' `integration_tests` are dropped here
        // the same way `test_target` is `None` for them.
        let integration_tests = if kind.has_lib() {
            collect_integration_test_views(m, &compile_options, &rewrite_root, &cmake_dir, &layout)
        } else {
            Vec::new()
        };

        members.push(MemberView {
            name: m.name.clone(),
            kind,
            lib_sources,
            bins,
            archive_output_dir: layout.build_dir(&m.name),
            runtime_output_dir: layout.profile_root.clone(),
            bin_include_dirs,
            workspace_link_deps,
            lib_workspace_deps: if kind.has_lib() {
                m.deps.clone()
            } else {
                Vec::new()
            },
            compile_options,
            test_target,
            integration_tests,
            rewrites,
            surface_commands,
            surface_cycles,
            crate_header,
            test_sidecars,
            test_runner,
        });
    }

    Ok(WorkspaceView {
        cust_version: env!("CARGO_PKG_VERSION").to_string(),
        c_standard: "23".to_string(),
        plugin_path: plugin.map(|p| p.path.clone()),
        cust_exe: std::env::current_exe().context("resolving cust binary path")?,
        members,
    })
}

/// V42D-14: build the per-member unit-test target (whenever the
/// member has a lib half). Recompiles the lib-half sources into
/// the test exe plus the driver-generated runner TU, tagged with
/// `-DCUST_TEST_BUILD=1`. `None` for bin-only members. Extracted
/// from `collect_view` to keep it under the line cap.
fn collect_test_target(
    m: &Member,
    kind: MemberKind,
    lib_sources: &[SourceFile],
    compile_options: &[String],
    cmake_dir: &Path,
    layout: &TargetLayout,
) -> Option<TestTargetView> {
    if !kind.has_lib() {
        return None;
    }
    // Lib-half sources (same as the lib target) plus the
    // driver-generated runner TU. The runner path is the contract
    // V42D-14 pins: cmake_dir/cust_test_main_<crate>.c.
    let mut sources = lib_sources.to_vec();
    sources.push(SourceFile {
        path: cmake_dir.join(format!("cust_test_main_{}.c", m.name)),
        object_depends: Vec::new(),
    });
    let mut test_opts = compile_options.to_vec();
    // The CUST_TEST_BUILD=1 macro is the existing v0.3.2 signal
    // that activates the prelude's test branch + makes the plugin
    // attach normal external linkage to [[cust::test]] decls (so
    // the runner TU's extern forward declarations can reach them).
    test_opts.push("-DCUST_TEST_BUILD=1".to_string());
    let include_dirs = vec![layout
        .crate_header_path(&m.name)
        .parent()
        .unwrap()
        .to_path_buf()];
    Some(TestTargetView {
        target_name: format!("{}__test", m.name),
        sources,
        include_dirs,
        // Test exes link against the same lib deps as the member's
        // bin half — workspace deps in topo order.
        link_deps: m.deps.clone(),
        compile_options: test_opts,
        runtime_output_dir: layout.test_build_dir(&m.name),
    })
}

/// v0.4.3 V43D-5: build the `IntegrationTestView`s for one
/// lib-bearing workspace member. One view per `tests/<stem>.c`,
/// in the member's already-sorted-by-stem order (V43D-1).
///
/// Each view's sources are the rewritten `tests/<stem>.c` (under
/// `.rewrite/<crate>/tests/`) plus the runner TU
/// (`cmake_dir/cust_itest_main_<crate>__<stem>.c`); the CUT's lib
/// sources are NOT recompiled (V43D-3 — they arrive via the
/// `lib<crate>.a` link). `compile_options` are the member's plus
/// `-DCUST_TEST_BUILD=1`.
fn collect_integration_test_views(
    m: &Member,
    compile_options: &[String],
    rewrite_root: &Path,
    cmake_dir: &Path,
    layout: &TargetLayout,
) -> Vec<IntegrationTestView> {
    let include_dir = layout
        .crate_header_path(&m.name)
        .parent()
        .unwrap()
        .to_path_buf();
    let mut itest_opts = compile_options.to_vec();
    itest_opts.push("-DCUST_TEST_BUILD=1".to_string());
    m.integration_tests
        .iter()
        .map(|it| {
            // Rewritten test source mirrors the lib sources'
            // `.rewrite/<crate>/<rel>` scheme; <rel> is
            // `tests/<stem>.c`.
            let rel = it.source.strip_prefix(&m.root).unwrap_or(&it.source);
            let rewritten = rewrite_root.join(&m.name).join(rel);
            let runner_tu = cmake_dir.join(format!("cust_itest_main_{}__{}.c", m.name, it.stem));
            IntegrationTestView {
                target_name: format!("{}__itest__{}", m.name, it.stem),
                output_name: it.stem.clone(),
                sources: vec![
                    SourceFile {
                        path: rewritten,
                        object_depends: Vec::new(),
                    },
                    SourceFile {
                        path: runner_tu,
                        object_depends: Vec::new(),
                    },
                ],
                include_dirs: vec![include_dir.clone()],
                // Link the CUT's own lib target; PUBLIC
                // propagation pulls in its transitive workspace
                // dep chain (V43D-3).
                link_deps: vec![m.name.clone()],
                compile_options: itest_opts.clone(),
                runtime_output_dir: layout.integration_test_build_dir(&m.name, &it.stem),
            }
        })
        .collect()
}

/// Build the per-target compile options for one member matching
/// what the v0.4.0 driver's `build_cflags` produced (minus the
/// trailing `-c -o <obj> <src>` that `CMake` supplies). Output is a
/// flat list of `CMake` list elements; multi-word flags like
/// `-include <path>` are emitted as two separate elements so
/// `CMake`'s argv reconstruction passes them to clang verbatim.
fn build_member_compile_options(
    profile: &ResolvedProfile,
    manifest: &Manifest,
    prelude: &Path,
    plugin: Option<&Plugin>,
) -> Vec<String> {
    let mut opts: Vec<String> = Vec::new();

    // Profile cflags (-O…, -g…, -fsanitize=…, [profile.<kind>]
    // extra-cflags). Same order build::build_cflags emits.
    opts.extend(profile.cflags());

    // [clang] extra-cflags.
    opts.extend(manifest.clang.extra_cflags.iter().cloned());

    // -fvisibility=hidden: same default the v0.3 prelude pinned.
    opts.push("-fvisibility=hidden".to_string());

    // -include <prelude>: two separate argv tokens (clang
    // requires them split). CMake preserves list order; no
    // SHELL: prefix needed because -include doesn't collide
    // with any other flag CMake adds.
    opts.push("-include".to_string());
    opts.push(prelude.display().to_string());

    if let Some(plugin) = plugin {
        // Plugin loader. SHELL: prefix prevents CMake from
        // splitting / re-ordering this around other -f flags
        // (V42D-5). Path is absolute, resolved by
        // plugin::discover() before collect_view ran.
        opts.push(format!("SHELL:-fplugin={}", plugin.path.display()));
    }

    // Always suppress `-Wunknown-attributes` so cust-attribute
    // decls don't drown the build in warnings even when the
    // plugin is loaded (V42D-5 defensive).
    opts.push("-Wno-unknown-attributes".to_string());

    opts
}

const fn map_kind(k: &CrateKind) -> MemberKind {
    match k {
        CrateKind::Lib { .. } => MemberKind::LibOnly,
        CrateKind::Bin { .. } => MemberKind::BinOnly,
        CrateKind::LibAndBin { .. } => MemberKind::LibAndBin,
    }
}

/// Output of [`collect_sources`] for one entry source (lib half or
/// one bin). `surface_commands` / `surface_cycles` / `header_frags`
/// are populated only for the lib half (`is_lib_half = true`); they
/// are empty for bins.
struct CollectedSources {
    sources: Vec<SourceFile>,
    rewrites: Vec<RewriteCommand>,
    surface_commands: Vec<SurfaceCommand>,
    /// v0.4.5 V45D-6: one entry per `[[cust::pub_repr]]` import
    /// cycle (SCC of size > 1). Empty for the acyclic common case.
    surface_cycles: Vec<SurfaceCycleCommand>,
    /// Topo-ordered fragment paths for the member's `crate-header`
    /// command (V45D-5). Empty for bins.
    header_frags: Vec<PathBuf>,
    /// v0.4.6 V46D-2: one `test-sidecar` command per lib module.
    /// Empty for bins.
    test_sidecars: Vec<TestSidecarCommand>,
    /// v0.4.6 V46D-2: the per-crate `test-runner` command. `Some`
    /// for the lib half, `None` for bins.
    test_runner: Option<TestRunnerCommand>,
}

/// v0.4.5 V45D-3/V45D-4: per-member context for building the
/// `RewriteCommand`s **and** `SurfaceCommand`s alongside the
/// `SourceFile` list. Shared by the lib + every bin of a member;
/// the only per-source difference is `is_bin_half`, passed
/// separately. The surface-only fields (`std` / `mid_cflags` /
/// `prelude` / `plugin`) are unused when building bin sources.
struct MemberGenCtx {
    /// `target/<profile>/.h-fragments/<crate>/`.
    frags_dir: PathBuf,
    /// `target/<profile>/deps/`.
    deps_root: PathBuf,
    /// The member's published `<crate>.h`.
    own_lib_header: PathBuf,
    /// Declared dependency names, sorted (V45D-15 determinism).
    deps: Vec<String>,
    /// Whether the member has a library half (carve-out gate).
    has_lib: bool,
    /// `-std=<value>` for surface compiles (V45D-15).
    std: String,
    /// Mid cflags (profile cflags + `[clang] extra-cflags`), in
    /// order (V45D-15).
    mid_cflags: Vec<String>,
    /// The materialised prelude header (`-include`d during the
    /// surface compile).
    prelude: PathBuf,
    /// The cust clang plugin `.so`, if any (also a surface
    /// `DEPENDS`).
    plugin: Option<PathBuf>,
}

/// Build the shared generation context for one member
/// (V45D-3/V45D-4).
fn member_gen_ctx(
    m: &Member,
    kind: MemberKind,
    layout: &TargetLayout,
    profile: &ResolvedProfile,
    prelude: &Path,
    plugin: Option<&Plugin>,
    default_std: &str,
) -> MemberGenCtx {
    let mut sorted_deps = m.deps.clone();
    sorted_deps.sort();
    let std = m
        .manifest
        .clang
        .std
        .clone()
        .unwrap_or_else(|| default_std.to_string());
    let mut mid_cflags = profile.cflags();
    mid_cflags.extend(m.manifest.clang.extra_cflags.iter().cloned());
    MemberGenCtx {
        frags_dir: layout.fragments_dir(&m.name),
        deps_root: layout.profile_root.join("deps"),
        own_lib_header: layout.crate_header_path(&m.name),
        deps: sorted_deps,
        has_lib: kind.has_lib(),
        std,
        mid_cflags,
        prelude: prelude.to_path_buf(),
        plugin: plugin.map(|p| p.path.clone()),
    }
}

#[allow(clippy::too_many_arguments)] // explicit-path collector; bundling these into a struct would just move the noise
fn collect_sources(
    crate_root: &Path,
    entry_source: &Path,
    crate_name: &str,
    rewrite_root: &Path,
    layout: &TargetLayout,
    rw_ctx: &MemberGenCtx,
    is_bin_half: bool,
    is_lib_half: bool,
) -> Result<CollectedSources> {
    let modules = modules::discover(crate_root, entry_source)
        .with_context(|| format!("discovering module graph for `{crate_name}`"))?;
    let mut out: Vec<SourceFile> = Vec::with_capacity(modules.len());
    let mut rewrites: Vec<RewriteCommand> = Vec::with_capacity(modules.len());
    for m in &modules {
        // Post-rewrite path: target/<profile>/.rewrite/<crate>/<rel>.
        // <rel> is the source's path relative to the crate root,
        // preserving src/<…>.c structure (V42D scope item shape +
        // design doc "Headline outcome" example).
        let rel = m
            .source_path
            .strip_prefix(crate_root)
            .unwrap_or(&m.source_path);
        let rewrite_path = rewrite_root.join(crate_name).join(rel);

        // OBJECT_DEPENDS: every `#cust use crate::<importee>`
        // edge resolves to the importee's fragment header
        // (V42D-6); every *declared* `#cust use <dep>;` additionally
        // depends on the dep's published crate header's PRODUCING
        // path (V45D-14 — `build/<dep>/include/<dep>.h`, not the
        // `deps/` symlink, so Ninja connects the upstream
        // crate-header command and a downstream TU never compiles
        // before its dep header exists). An *undeclared* `#cust use
        // <x>;` (or a lib-half self-import) gets **no** edge: it is
        // a validation error the `rewrite-file` command surfaces
        // cleanly, and emitting an edge to a non-existent producer
        // would instead fail as an opaque Ninja "missing build
        // input" / dependency-cycle error (the bin-half own-crate
        // carve-out is the one self-reference that is valid).
        let mut object_depends: Vec<PathBuf> = m
            .imports
            .iter()
            .map(|importee| layout.fragment_path(crate_name, importee))
            .collect();
        for dep in &m.dep_imports {
            if dep_edge_is_valid(dep, crate_name, rw_ctx, is_bin_half) {
                object_depends.push(layout.crate_header_path(dep));
            }
        }

        // V45D-3: one rewrite custom command per source.
        rewrites.push(RewriteCommand {
            out: rewrite_path.clone(),
            origin: m.source_path.clone(),
            crate_name: crate_name.to_string(),
            frags_dir: rw_ctx.frags_dir.clone(),
            deps_root: rw_ctx.deps_root.clone(),
            own_lib_header: rw_ctx.own_lib_header.clone(),
            deps: rw_ctx.deps.clone(),
            is_bin_half,
            has_lib: rw_ctx.has_lib,
        });

        // V45D-4/V45D-6: surface commands are emitted per SCC
        // *after* this loop (a cyclic SCC becomes one coarse
        // command), not per source here.

        out.push(SourceFile {
            path: rewrite_path,
            object_depends,
        });
    }

    // V45D-4/V45D-6: surface generation, grouped by strongly-
    // connected component of the intra-crate import graph (lib half
    // only — bins are never surface-passed). A singleton SCC is an
    // acyclic module → one fine-grained `surface-module` command; an
    // SCC of size > 1 is a `[[cust::pub_repr]]` cycle → one coarse
    // `surface-cycle` command covering all its modules. For an
    // all-acyclic crate `module_sccs` returns singletons in
    // discovery order, so the fine-grained command order is
    // unchanged from V45D-4.
    let mut surface_commands: Vec<SurfaceCommand> = Vec::new();
    let mut surface_cycles: Vec<SurfaceCycleCommand> = Vec::new();
    if is_lib_half {
        for scc in crate::build::module_sccs(&modules) {
            if scc.len() == 1 {
                surface_commands.push(surface_command_for(
                    &modules[scc[0]],
                    crate_name,
                    layout,
                    rw_ctx,
                ));
            } else {
                surface_cycles.push(surface_cycle_for(
                    &modules, &scc, crate_name, layout, rw_ctx,
                ));
            }
        }
    }

    // V45D-5: the crate-header command's `--frag` list, in
    // topological order over the intra-crate import edges (same
    // order `build::write_crate_header` concatenates). Lib half
    // only.
    let header_frags = if is_lib_half {
        crate::build::topo_order_modules(&modules)
            .iter()
            .map(|m| layout.fragment_path(crate_name, &m.qualified_name))
            .collect()
    } else {
        Vec::new()
    };

    // V46D-2: per-module unit test-sidecar commands + the per-crate
    // test-runner command (lib half only — bins have no tests). The
    // sidecar list is in module-discovery order, matching the
    // in-process runner-TU test ordering so the rendered runner is
    // byte-identical.
    let (test_sidecars, test_runner) = if is_lib_half {
        let mut sidecars: Vec<TestSidecarCommand> = Vec::with_capacity(modules.len());
        let mut sidecar_paths: Vec<PathBuf> = Vec::with_capacity(modules.len());
        for m in &modules {
            let sc = test_sidecar_command_for(m, crate_name, layout, rw_ctx);
            sidecar_paths.push(sc.sidecar_out.clone());
            sidecars.push(sc);
        }
        let runner = TestRunnerCommand {
            crate_name: crate_name.to_string(),
            out: layout
                .profile_root
                .join("cmake")
                .join(format!("cust_test_main_{crate_name}.c")),
            sidecars: sidecar_paths,
        };
        (sidecars, Some(runner))
    } else {
        (Vec::new(), None)
    };

    Ok(CollectedSources {
        sources: out,
        rewrites,
        surface_commands,
        surface_cycles,
        header_frags,
        test_sidecars,
        test_runner,
    })
}

/// v0.4.5 V45D-4: whether a `#cust use <dep>;` should produce a
/// build-graph edge to `build/<dep>/include/<dep>.h`. Mirrors the
/// `rewrite_one` validation carve-out exactly: a declared
/// dependency always resolves; the bin half of a lib+bin crate may
/// additionally self-reference its own crate (lowered to the own
/// lib header). Anything else (an undeclared dep, or a lib-half
/// self-import) is a validation error the `rewrite-file` command
/// surfaces — emitting a graph edge for it would instead fail as
/// an opaque Ninja "missing build input" or dependency cycle.
fn dep_edge_is_valid(
    dep: &str,
    crate_name: &str,
    rw_ctx: &MemberGenCtx,
    is_bin_half: bool,
) -> bool {
    rw_ctx.deps.iter().any(|d| d == dep) || (is_bin_half && rw_ctx.has_lib && dep == crate_name)
}

/// v0.4.5 V45D-4: build the `SurfaceCommand` for one lib module.
/// `import_fragments` / `dep_headers` (the `DEPENDS` edges) are
/// emitted sorted for byte-stable output (V45D-15).
fn surface_command_for(
    m: &modules::Module,
    crate_name: &str,
    layout: &TargetLayout,
    rw_ctx: &MemberGenCtx,
) -> SurfaceCommand {
    let surface_out = layout
        .build_dir(crate_name)
        .join(format!("{}.surface.c", m.qualified_name));
    let src_dir = m
        .source_path
        .parent()
        .map_or_else(|| PathBuf::from("."), Path::to_path_buf);

    let mut import_fragments: Vec<PathBuf> = m
        .imports
        .iter()
        .map(|importee| layout.fragment_path(crate_name, importee))
        .collect();
    import_fragments.sort();

    let mut dep_headers: Vec<PathBuf> = m
        .dep_imports
        .iter()
        .filter(|dep| dep_edge_is_valid(dep, crate_name, rw_ctx, false))
        .map(|dep| layout.crate_header_path(dep))
        .collect();
    dep_headers.sort();

    SurfaceCommand {
        source: m.source_path.clone(),
        surface_out,
        fragment_out: layout.fragment_path(crate_name, &m.qualified_name),
        frags_dir: rw_ctx.frags_dir.clone(),
        deps_root: rw_ctx.deps_root.clone(),
        deps: rw_ctx.deps.clone(),
        std: rw_ctx.std.clone(),
        cflags: rw_ctx.mid_cflags.clone(),
        includes: vec![src_dir],
        prelude: rw_ctx.prelude.clone(),
        plugin: rw_ctx.plugin.clone(),
        import_fragments,
        dep_headers,
    }
}

/// v0.4.6 V46D-2: build the unit `TestSidecarCommand` for one lib
/// module. Mirrors [`surface_command_for`]'s `DEPENDS` shape
/// (imported build-mode fragments + cross-crate dep headers, both
/// sorted — V45D-15/V46D-7) but targets the `.cust.tests` sidecar
/// instead of the fragment, and uses a distinct `.test-surface.c`
/// scratch path so it never races the build-mode surface command.
fn test_sidecar_command_for(
    m: &modules::Module,
    crate_name: &str,
    layout: &TargetLayout,
    rw_ctx: &MemberGenCtx,
) -> TestSidecarCommand {
    let surface_out = layout
        .build_dir(crate_name)
        .join(format!("{}.test-surface.c", m.qualified_name));
    let src_dir = m
        .source_path
        .parent()
        .map_or_else(|| PathBuf::from("."), Path::to_path_buf);

    let mut import_fragments: Vec<PathBuf> = m
        .imports
        .iter()
        .map(|importee| layout.fragment_path(crate_name, importee))
        .collect();
    import_fragments.sort();

    let mut dep_headers: Vec<PathBuf> = m
        .dep_imports
        .iter()
        .filter(|dep| dep_edge_is_valid(dep, crate_name, rw_ctx, false))
        .map(|dep| layout.crate_header_path(dep))
        .collect();
    dep_headers.sort();

    TestSidecarCommand {
        crate_name: crate_name.to_string(),
        module: m.qualified_name.clone(),
        source: m.source_path.clone(),
        surface_out,
        sidecar_out: layout.test_sidecar_path(
            crate_name,
            TestOrigin::Unit {
                qualified_name: &m.qualified_name,
            },
        ),
        frags_dir: rw_ctx.frags_dir.clone(),
        deps_root: rw_ctx.deps_root.clone(),
        deps: rw_ctx.deps.clone(),
        std: rw_ctx.std.clone(),
        cflags: rw_ctx.mid_cflags.clone(),
        includes: vec![src_dir],
        prelude: rw_ctx.prelude.clone(),
        plugin: rw_ctx.plugin.clone(),
        import_fragments,
        dep_headers,
    }
}

/// v0.4.5 V45D-6: build the single `SurfaceCycleCommand` for one
/// cyclic SCC. `scc` holds the indices (into `modules`) of every
/// member of the cycle. The command's `OUTPUT` is all their
/// fragments; `external_deps` are the fragments the cycle imports
/// from *outside* the SCC plus cross-crate dep headers (intra-cycle
/// imports are excluded — the command produces them). All repeated
/// lists are sorted for byte-stable output (V45D-15).
fn surface_cycle_for(
    modules: &[modules::Module],
    scc: &[usize],
    crate_name: &str,
    layout: &TargetLayout,
    rw_ctx: &MemberGenCtx,
) -> SurfaceCycleCommand {
    use std::collections::BTreeSet;

    // Set of qnames in this SCC, for the intra-cycle membership test.
    let in_cycle: BTreeSet<&str> = scc
        .iter()
        .map(|&i| modules[i].qualified_name.as_str())
        .collect();

    // Cycle members, sorted by qualified name (V45D-15).
    let mut members: Vec<&modules::Module> = scc.iter().map(|&i| &modules[i]).collect();
    members.sort_by(|a, b| a.qualified_name.cmp(&b.qualified_name));

    let module_pairs: Vec<(String, PathBuf)> = members
        .iter()
        .map(|m| (m.qualified_name.clone(), m.source_path.clone()))
        .collect();
    let mut fragment_outs: Vec<PathBuf> = members
        .iter()
        .map(|m| layout.fragment_path(crate_name, &m.qualified_name))
        .collect();
    fragment_outs.sort();

    // External `DEPENDS`: imports that point outside the SCC, plus
    // valid cross-crate dep headers. Intra-cycle imports excluded.
    let mut external: BTreeSet<PathBuf> = BTreeSet::new();
    for m in &members {
        for importee in &m.imports {
            if !in_cycle.contains(importee.as_str()) {
                external.insert(layout.fragment_path(crate_name, importee));
            }
        }
        for dep in &m.dep_imports {
            if dep_edge_is_valid(dep, crate_name, rw_ctx, false) {
                external.insert(layout.crate_header_path(dep));
            }
        }
    }

    SurfaceCycleCommand {
        crate_name: crate_name.to_string(),
        modules: module_pairs,
        fragment_outs,
        frags_dir: rw_ctx.frags_dir.clone(),
        scratch_dir: layout.build_dir(crate_name),
        deps_root: rw_ctx.deps_root.clone(),
        deps: rw_ctx.deps.clone(),
        std: rw_ctx.std.clone(),
        cflags: rw_ctx.mid_cflags.clone(),
        prelude: rw_ctx.prelude.clone(),
        plugin: rw_ctx.plugin.clone(),
        external_deps: external.into_iter().collect(),
    }
}

/// Write the workspace's `CMakeLists` into the build tree
/// (V42D-13 path + V42D-8 configure-skip stamp).
///
/// Slice A's standalone emit entry point; slice B's
/// `emit_and_drive_cmake` is now the production caller, so this
/// stays around mainly for tests and out-of-band tooling.
#[allow(dead_code)] // superseded as a build-path callee by `emit_and_drive_cmake`; kept for tests
pub fn emit_workspace_cmakelists(
    ws: &Workspace,
    profile_kind: ProfileKind,
    plugin: Option<&Plugin>,
) -> Result<WriteOutcome> {
    // No `Clang` handle on this (test/out-of-band) path; discover
    // one for the surface std fallback (V45D-15). Cheap — a single
    // `clang --version` probe.
    let clang = crate::clang::Clang::discover()?;
    let view = collect_view(ws, profile_kind, plugin, clang.default_std())?;
    let bytes = generate(&view).into_bytes();
    let layout = TargetLayout::for_workspace(&ws.root, profile_kind);
    let cmake_dir = layout.profile_root.join("cmake");
    let cmakelists = cmake_dir.join("CMakeLists.txt");
    let stamp = cmake_dir.join("stamp").join("cmakelists.sha256");
    write_if_changed(&cmakelists, &bytes, &stamp, view.plugin_path.as_deref())
}

// ─── Slice B: drive cmake + ninja ───────────────────────────────

/// Inputs to `emit_and_drive_cmake`. Mirror of the v0.4.2 design
/// doc's `cust build` / `cust build -p <name>` shape.
#[derive(Default)]
pub struct DriveOptions<'a> {
    /// `Some(name)` lowers to `cmake --build --target <name>`.
    /// `None` builds every default target. Slice B accepts a
    /// single member name; v0.4.4 multi-bin scopes further via
    /// `bin`.
    pub only: Option<&'a str>,
    /// v0.4.4 V44D-7: `Some(bin)` scopes the build to the single
    /// binary named `bin` within `only`'s member — lowered to
    /// `cmake --build --target <that-bin's-target>`. `None` builds
    /// all of the member's (or workspace's) default targets.
    pub bin: Option<&'a str>,
    /// `cmake --build -j <jobs>`. `None` lets `Ninja` pick (`Ninja`
    /// defaults to `nproc`). Slice D will plumb `--jobs` / env
    /// vars through here.
    pub jobs: Option<u32>,
    /// V42D-14: when `true`, the build step picks the
    /// `<member>__test` targets instead of the default lib +
    /// bin ones (which are `EXCLUDE_FROM_ALL` for test targets).
    /// The emitted `CMakeLists` is identical either way, so
    /// flipping modes neither churns the V42D-8 stamp nor
    /// triggers a `CMake` reconfigure.
    pub test_build: bool,
}

/// One-shot orchestrator for the v0.4.2 `CMake`-driven build path.
/// Emits the workspace `CMakeLists`, optionally reconfigures, and
/// drives `cmake --build`. Returns once `Ninja` exits.
///
/// Steps (per V42D-16):
///
/// 1. `find_cmake()` / `find_ninja()` — install hints on miss.
/// 2. `collect_view` → `generate` → `write_if_changed`.
/// 3. Configure: if `WriteOutcome::Wrote` *or* the build dir
///    doesn't already contain a `CMakeCache.txt`, run
///    `cmake -G `Ninja` -S <cmake_dir> -B <build_dir>
///    -DCMAKE_C_COMPILER=<abs/clang>`. Otherwise skip (V42D-8:
///    stamp matched, nothing to reconfigure).
/// 4. Build: `cmake --build <build_dir>` (with `--target <name>`
///    when `opts.only` is set; with `-j <jobs>` when set).
/// 5. Publish `compile_commands.json` (V42D-12): symlink
///    `target/<profile>/compile_commands.json` and the legacy
///    `target/compile_commands.json` to the `CMake`-emitted
///    `<build_dir>/compile_commands.json`.
///
/// Both cmake and ninja get their stdout + stderr piped through
/// `diag_rewrite::rewrite` (V42D-18).
pub fn emit_and_drive_cmake(
    ws: &Workspace,
    profile_kind: ProfileKind,
    clang: &crate::clang::Clang,
    plugin: Option<&Plugin>,
    opts: &DriveOptions<'_>,
) -> Result<()> {
    let cmake = find_cmake()?;
    let _ninja = find_ninja()?; // discovery + version-parse check; CMake invokes ninja itself

    let layout = TargetLayout::for_workspace(&ws.root, profile_kind);
    let cmake_dir = layout.profile_root.join("cmake");
    let build_dir = cmake_dir.join("build");
    let cmakelists = cmake_dir.join("CMakeLists.txt");
    let stamp = cmake_dir.join("stamp").join("cmakelists.sha256");

    let view = collect_view(ws, profile_kind, plugin, clang.default_std())?;
    let bytes = generate(&view).into_bytes();
    let outcome = write_if_changed(&cmakelists, &bytes, &stamp, view.plugin_path.as_deref())?;

    // V42D-14: ensure every member's integration-test runner TU
    // exists on disk before configure. CMake validates every
    // `add_executable` source path at configure time even when the
    // target is `EXCLUDE_FROM_ALL`, so a missing runner TU during
    // `cust build` would fail configure. The build path doesn't
    // need real runner content (the target is never built); a tiny
    // stub is enough.
    //
    // v0.4.6 V46D-2: the *unit* runner TU
    // (`cust_test_main_<crate>.c`) is now produced by the
    // `cust internal test-runner` custom command, so CMake treats
    // it as GENERATED and the stub for it is redundant (harmless —
    // the command overwrites it). Integration runner TUs are still
    // driver-generated (V46D-3 / slice C), so they still need the
    // stub.
    ensure_runner_tu_stubs(&view, &cmake_dir)?;

    // Configure if the CMakeLists was just written OR there's no
    // existing CMake cache to reuse. Either condition forces a
    // fresh `cmake -G Ninja` run. Mode flips between `cust
    // build` and `cust test` are NOT a reason to reconfigure:
    // the same CMakeLists drives both — test targets are
    // `EXCLUDE_FROM_ALL` and selected per-invocation via
    // `cmake --build --target` below.
    let must_configure =
        outcome == WriteOutcome::Wrote || !build_dir.join("CMakeCache.txt").is_file();
    if must_configure {
        fs::create_dir_all(&build_dir)
            .with_context(|| format!("creating `{}`", build_dir.display()))?;
        // Wipe any stale cache so a compiler-path change (or any
        // other -D the driver might add later) takes effect on the
        // next project() call. Object files under CMakeFiles/ stay.
        let _ = fs::remove_file(build_dir.join("CMakeCache.txt"));
        let mut cmd = Command::new(&cmake.path);
        cmd.arg("-G").arg("Ninja");
        cmd.arg("-S").arg(&cmake_dir);
        cmd.arg("-B").arg(&build_dir);
        // V42D-3 / Headline outcome: cust drives clang, not the
        // system default. Passing on the configure line means
        // CMake records it in CMakeCache.txt and uses it for
        // every subsequent `cmake --build`.
        cmd.arg(format!("-DCMAKE_C_COMPILER={}", clang.path.display()));
        run_streaming(cmd, "cmake configure")?;
    }

    // Build.
    let mut cmd = Command::new(&cmake.path);
    cmd.arg("--build").arg(&build_dir);
    if let Some(bin_name) = opts.bin {
        // v0.4.4 V44D-7: `--bin <name>` scopes to the single bin's
        // CMake target (CMake pulls its transitive lib deps).
        // `opts.only` (if set) restricts the search to one member;
        // the CLI has already validated existence + uniqueness.
        let target = bin_target_name_for(&view, opts.only, bin_name)
            .ok_or_else(|| anyhow::anyhow!("internal: no CMake target for bin `{bin_name}`"))?;
        cmd.arg("--target").arg(target);
    } else if let Some(target) = opts.only {
        // For lib+bin members the bin lives at a `-bin`-suffixed
        // CMake target name; the lib keeps the unsuffixed
        // member name. `-p <name>` builds both halves — or, in
        // test mode, just the `<name>__test` target.
        for cmake_target in target_names_for(&view, target, opts.test_build) {
            cmd.arg("--target").arg(cmake_target);
        }
    } else if opts.test_build {
        // No `-p`: in test mode build only the `__test` targets,
        // not the lib + bin defaults. Mirrors Cargo's `cargo
        // test` which doesn't link production bins.
        for m in &view.members {
            if let Some(t) = &m.test_target {
                cmd.arg("--target").arg(&t.target_name);
            }
            // v0.4.3 V43D-5: also build each integration-test exe.
            for it in &m.integration_tests {
                cmd.arg("--target").arg(&it.target_name);
            }
        }
    }
    if let Some(jobs) = opts.jobs {
        cmd.arg("-j").arg(jobs.to_string());
    }
    run_streaming(cmd, "cmake --build")?;

    publish_compile_commands(&layout, &build_dir)?;
    Ok(())
}

/// v0.4.4 V44D-7: resolve `--bin <name>` to its `CMake` target
/// name. Searches `member`'s bins (or every member when `member`
/// is `None`) for the `BinView` whose `output_name` matches
/// `bin_name`, returning the first match's `target_name`. The CLI
/// validates existence + cross-member uniqueness before the build,
/// so a single match is expected here.
fn bin_target_name_for<'a>(
    view: &'a WorkspaceView,
    member: Option<&str>,
    bin_name: &str,
) -> Option<&'a str> {
    view.members
        .iter()
        .filter(|m| member.is_none_or(|name| m.name == name))
        .flat_map(|m| &m.bins)
        .find(|b| b.output_name == bin_name)
        .map(|b| b.target_name.as_str())
}

/// `CMake` target name(s) for a member, used when lowering
/// `cust build -p <name>` to `cmake --build --target ...`.
/// Returns `[lib]`, `[bin]`, or `[lib, bin]` for build mode;
/// `[<name>__test]` for test mode (V42D-14). Unknown member
/// names produce an empty Vec (the caller fails at the
/// workspace-membership check before reaching this).
fn target_names_for<'a>(
    view: &'a WorkspaceView,
    member_name: &str,
    test_mode: bool,
) -> Vec<&'a str> {
    let Some(m) = view.members.iter().find(|m| m.name == member_name) else {
        return Vec::new();
    };
    if test_mode {
        let mut out = Vec::new();
        if let Some(t) = &m.test_target {
            out.push(t.target_name.as_str());
        }
        // v0.4.3 V43D-5: include the member's integration exes.
        out.extend(m.integration_tests.iter().map(|it| it.target_name.as_str()));
        return out;
    }
    let mut out = Vec::with_capacity(1 + m.bins.len());
    if m.kind.has_lib() {
        out.push(m.name.as_str());
    }
    // v0.4.4 V44D-5: every bin of the member.
    out.extend(m.bins.iter().map(|b| b.target_name.as_str()));
    out
}

/// V42D-14: ensure each member's runner TU exists on disk before
/// `CMake` configure runs. `CMake` validates every
/// `add_executable` source path at configure time even for
/// `EXCLUDE_FROM_ALL` targets, so a missing runner TU would fail
/// configure during `cust build`.
///
/// v0.4.6 V46D-2: the unit runner TU (`cust_test_main_<crate>.c`)
/// is now a `cust internal test-runner` custom-command OUTPUT, so
/// `CMake` knows it is GENERATED and won't validate its existence
/// — the stub written here for it is harmless redundancy (the
/// command overwrites it on the test build). The integration
/// runner TUs (`cust_itest_main_<crate>__<stem>.c`) are still
/// driver-generated (V46D-3 / slice C) and genuinely need the
/// stub.
///
/// Idempotent + cheap: stubs are only written when the runner
/// TU file is missing, and the stub content is small enough
/// that any subsequent `write_if_byte_different` from the test
/// path or the Ninja command always replaces it.
fn ensure_runner_tu_stubs(view: &WorkspaceView, cmake_dir: &Path) -> Result<()> {
    const STUB: &[u8] = b"/* @generated by cust \xe2\x80\x94 build-mode stub.\n\
 * The real runner is rendered by the `cust internal test-runner`\n\
 * custom command (unit) or `cust test`'s driver path (integration)\n\
 * before this target is built; see cust/src/test_runner.rs.\n\
 */\n";
    for m in &view.members {
        let Some(t) = &m.test_target else { continue };
        // The runner TU is the only source under `cmake_dir/`
        // (the lib sources all live under `.rewrite/<crate>/`).
        for src in &t.sources {
            if src.path.starts_with(cmake_dir) && !src.path.is_file() {
                if let Some(parent) = src.path.parent() {
                    fs::create_dir_all(parent)
                        .with_context(|| format!("creating `{}`", parent.display()))?;
                }
                fs::write(&src.path, STUB)
                    .with_context(|| format!("writing runner stub `{}`", src.path.display()))?;
            }
        }
    }
    // v0.4.3 V43D-5: same treatment for each integration-test
    // exe's runner TU (`cust_itest_main_<crate>__<stem>.c`,
    // under `cmake_dir/`). The rewritten `tests/<stem>.c` source
    // lives under `.rewrite/<crate>/tests/` and is materialised
    // by the test path (Slice C), not stubbed here.
    for m in &view.members {
        for it in &m.integration_tests {
            for src in &it.sources {
                if src.path.starts_with(cmake_dir) && !src.path.is_file() {
                    if let Some(parent) = src.path.parent() {
                        fs::create_dir_all(parent)
                            .with_context(|| format!("creating `{}`", parent.display()))?;
                    }
                    fs::write(&src.path, STUB)
                        .with_context(|| format!("writing runner stub `{}`", src.path.display()))?;
                }
            }
        }
    }
    Ok(())
}

/// Publish `CMake`'s `compile_commands.json` at both the canonical
/// V42D-12 location (`target/<profile>/compile_commands.json`)
/// and the legacy v0.1 location (`target/compile_commands.json`)
/// via symlinks. The legacy location stays during v0.4.x because
/// existing dev tooling and the v0.1+ test suite reach for it.
fn publish_compile_commands(layout: &TargetLayout, build_dir: &Path) -> Result<()> {
    let canonical = build_dir.join("compile_commands.json");
    if !canonical.is_file() {
        return Ok(()); // configure was skipped and an old cache lacks the export — non-fatal
    }
    let v42d12_path = layout.profile_root.join("compile_commands.json");
    let legacy_path = layout.target_root.join("compile_commands.json");
    symlink_overwrite(&canonical, &v42d12_path)?;
    symlink_overwrite(&canonical, &legacy_path)?;
    Ok(())
}

fn symlink_overwrite(target: &Path, link: &Path) -> Result<()> {
    if let Some(parent) = link.parent() {
        fs::create_dir_all(parent).with_context(|| format!("creating `{}`", parent.display()))?;
    }
    if fs::symlink_metadata(link).is_ok() {
        fs::remove_file(link).with_context(|| format!("removing stale `{}`", link.display()))?;
    }
    #[cfg(unix)]
    std::os::unix::fs::symlink(target, link)
        .with_context(|| format!("symlinking `{}` -> `{}`", link.display(), target.display()))?;
    #[cfg(not(unix))]
    fs::copy(target, link)
        .map(|_| ())
        .with_context(|| format!("copying `{}` -> `{}`", target.display(), link.display()))?;
    Ok(())
}

// ─── Subprocess wrapper + diagnostic rewrite ────────────────────
//
// Inline helper per V42D-18 (the design says "promote to a shared
// module only if a second caller appears"). Spawns `cmd` with
// piped stdout + stderr; one reader thread per pipe pushes each
// line through `diag_rewrite::rewrite` before printing to the
// driver's own stdout / stderr.

fn run_streaming(mut cmd: Command, ctx: &'static str) -> Result<()> {
    use std::io::{BufRead, BufReader};

    cmd.stdin(Stdio::null())
        .stdout(Stdio::piped())
        .stderr(Stdio::piped());

    let mut child = cmd.spawn().with_context(|| format!("spawning {ctx}"))?;

    let stdout = child.stdout.take().expect("piped stdout");
    let stderr = child.stderr.take().expect("piped stderr");

    let h_out = std::thread::spawn(move || {
        let reader = BufReader::new(stdout);
        for line in reader.lines().map_while(Result::ok) {
            let rewritten = crate::diag_rewrite::rewrite(&line);
            println!("{rewritten}");
        }
    });
    let h_err = std::thread::spawn(move || {
        let reader = BufReader::new(stderr);
        for line in reader.lines().map_while(Result::ok) {
            let rewritten = crate::diag_rewrite::rewrite(&line);
            eprintln!("{rewritten}");
        }
    });

    let status = child.wait().with_context(|| format!("waiting on {ctx}"))?;
    // Joining the reader threads is fire-and-forget: the pipes
    // closed when the child exited, so the threads finish on
    // their own; we just sync before returning so the driver
    // doesn't print its own "Finished" line before the child's
    // last lines flush.
    let _ = h_out.join();
    let _ = h_err.join();

    if !status.success() {
        bail!("{ctx} failed with status {status}");
    }
    Ok(())
}

